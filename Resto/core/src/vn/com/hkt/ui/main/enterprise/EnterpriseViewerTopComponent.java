    @Override
    public void load() {
        enterpriseViewer = Lookup.getDefault().lookupAll(IEnterpriseViewer.class);
        enterpriseExtViewer = Lookup.getDefault().lookupAll(IEnterpriseExtViewer.class);
        loadToCombo();
        cboSelectView.setModel(enterpriseModel);
        if ((enterpriseViewer != null)) {
            panelForm.setLayout(new GridLayout());
            Object selected = cboSelectView.getItemAt(0);
            IEnterpriseViewer middle = (IEnterpriseViewer) selected;
            fillCombo(middle);
            JPanel p1 = middle.getEnterpriseViewer();
            panelForm.removeAll();
            panelForm.add(p1);
            if (selected instanceof IReportListGUI) {
                panelReport = (IReportListGUI) selected;
            }
            cboSelectView.setEnabled(true);
        }
        loadView();
    }

    public void loadView() {
        for (IEnterpriseViewer iev : enterpriseViewer) {
            if (iev instanceof ViewCookieList) {
                try {
                    ((ViewCookieList) iev).ViewCookieList();
                } catch (IOException ex) {
                    Exceptions.printStackTrace(ex);
                }
            }
        }
        for (IEnterpriseExtViewer iexv : enterpriseExtViewer) {
            if (iexv instanceof ViewCookieList) {
                try {
                    ((ViewCookieList) iexv).ViewCookieList();
                } catch (IOException ex) {
                    Exceptions.printStackTrace(ex);
                }
            }
        }
        reLoadFont();
        reLoadSize();
        reLoadColor();
        reLoadColorMouse();
        reLoadColorTitle();
        reLoadColorWord();
    }

    public void remove() {
        Object selected = cboSelectView.getSelectedItem();
        if (selected != null) {
            if (selected instanceof RemoveCookie) {
                try {
                    ((RemoveCookie) selected).remove();
                } catch (IOException ex) {
                    Exceptions.printStackTrace(ex);
                }
            }
        }
    }

    public void editList() {
        for (IEnterpriseViewer iev : enterpriseViewer) {
            if (iev instanceof EditCookieList) {
                try {
                    ((EditCookieList) iev).EditCookieList(isEdit);
                } catch (IOException ex) {
                    Exceptions.printStackTrace(ex);
                }
            }
        }
        for (IEnterpriseExtViewer iexv : enterpriseExtViewer) {
            if (iexv instanceof EditCookieList) {
                try {
                    ((EditCookieList) iexv).EditCookieList(isEdit);
                } catch (IOException ex) {
                    Exceptions.printStackTrace(ex);
                }
            }
        }
    }

    public void saveList() {
        for (IEnterpriseViewer iev : enterpriseViewer) {
            if (iev instanceof SaveCookieList) {
                try {
                    ((SaveCookieList) iev).SaveCookieList();
                } catch (IOException ex) {
                    Exceptions.printStackTrace(ex);
                }
            }
        }
        for (IEnterpriseExtViewer iexv : enterpriseExtViewer) {
            if (iexv instanceof SaveCookieList) {
                try {
                    ((SaveCookieList) iexv).SaveCookieList();
                } catch (IOException ex) {
                    Exceptions.printStackTrace(ex);
                }
            }
        }
    }

    protected void loadToCombo() {
        enterpriseModel = new DefaultComboBoxModel();
        if (!enterpriseViewer.isEmpty()) {
            Iterator it = enterpriseViewer.iterator();
            while (it.hasNext()) {
                enterpriseModel.addElement(it.next());
            }
        }
        if (!enterpriseExtViewer.isEmpty()) {
            Iterator it = enterpriseExtViewer.iterator();
            while (it.hasNext()) {
                enterpriseModel.addElement(it.next());
            }
        }

    }

    private void reLoadFont() {
        for (IEnterpriseViewer iev : enterpriseViewer) {
            if (iev instanceof IResetFontSize) {
                ((IResetFontSize) iev).resetFont();
            }
        }
        for (IEnterpriseExtViewer editCookie : enterpriseExtViewer) {
            if (editCookie instanceof IResetFontSize) {
                ((IResetFontSize) editCookie).resetFont();
            }
        }
    }

