    private void cboSelectViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cboSelectViewActionPerformed
        // TODO add your handling code here:
        for (int i = 0; i < enterpriseModel.getSize(); i++) {
            Object selected = cboSelectView.getItemAt(i);
            if (selected instanceof IEnterpriseExtViewer) {
                IEnterpriseExtViewer p = (IEnterpriseExtViewer) selected;
                p.getTable().clearSelection();
            }
            if (selected instanceof IEnterpriseViewer) {
                IEnterpriseViewer p = (IEnterpriseViewer) selected;
                p.getTable().clearSelection();
            }
        }
        panelForm.setLayout(new GridLayout());
        Object selected = cboSelectView.getSelectedItem();
        if (selected instanceof IEnterpriseExtViewer) {
            IEnterpriseExtViewer p = (IEnterpriseExtViewer) selected;
            fillCombo(p);
            JPanel p2 = p.getEnterpriseExtViewer();
            panelForm.removeAll();
            panelForm.setLayout(new GridLayout());
            panelForm.add(p2);
        }
        if (selected instanceof IEnterpriseViewer) {
            IEnterpriseViewer p = (IEnterpriseViewer) selected;
            fillCombo(p);
            JPanel p2 = p.getEnterpriseViewer();
            panelForm.removeAll();
            panelForm.add(p2);
        }
        if (selected instanceof IReportListGUI) {
            panelReport = (IReportListGUI) selected;
        } else {
            panelReport = null;
        }

    }//GEN-LAST:event_cboSelectViewActionPerformed

